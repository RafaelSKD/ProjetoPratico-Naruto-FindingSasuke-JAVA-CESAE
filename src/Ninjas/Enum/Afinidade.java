package Ninjas.Enum;

/**
 * Enumeração que representa os tipos de afinidade elemental dos ninjas.
 * Essas afinidades são usadas para determinar o estilo de jutsus e ataques
 * que cada ninja pode usar com maior eficácia.
 */
public enum Afinidade {
    /** Afinidade com o elemento Fogo (Katon) */
    FOGO,

    /** Afinidade com o elemento Vento (Fūton) */
    VENTO,

    /** Afinidade com o elemento Relâmpago (Raiton) */
    RELAMPAGO,

    /** Afinidade com o elemento Água (Suiton) */
    AGUA,

    /** Afinidade com o elemento Terra (Doton) */
    TERRA
}
