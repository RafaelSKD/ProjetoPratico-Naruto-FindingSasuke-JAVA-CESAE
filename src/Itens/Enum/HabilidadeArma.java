package Itens.Enum;

/**
 * Enumeração que define os tipos de habilidades que uma arma pode ter.
 * Cada tipo indica o efeito principal que a arma causará ao ser usada.
 */
public enum HabilidadeArma {
    ATAQUE,   // Arma que enfraquece ou interfere no ataque do oponente
    DEFESA,   // Arma que enfraquece ou contorna a defesa do oponente
    ESPECIAL  // Arma com efeito especial não convencional
}
