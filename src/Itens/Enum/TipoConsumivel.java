package Itens.Enum;

/**
 * Enumeração que define os tipos de efeitos que um item consumível pode ter.
 * Cada tipo afeta um atributo específico do utilizador.
 */
public enum TipoConsumivel {
    ATAQUE,  // Aumenta o poder de ataque temporariamente
    VIDA,    // Restaura a vida do utilizador
    CHAKRA   // Recupera o chakra necessário para habilidades especiais
}
